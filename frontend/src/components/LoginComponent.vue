
<template>
    <div class="container background" style="margin-top: 50px;">
        <div class="row">
            <div class="offset-md-3 col-md-6">
                <form method="POST" v-on:submit.prevent="doLogin">
                    <div class="form-group">
                        <label class="text-white">Enter email</label>
                        <input type="email" class="form-control" name="email" />
                    </div>
 
                    <br />
 
                    <div class="form-group">
                        <label class="text-white">Enter password</label>
                        <input type="password" class="form-control" name="password" />
                    </div>
 
                    <br />
 
                    <input type="submit" v-bind:value="isLoading ? 'Loading...' : 'Login'" v-bind:disabled="isLoading" name="submit" class="btn btn-primary" />
                </form>
                <br />
                <div class="register-button">      
                    Do not have an account?&nbsp;<a><router-link class="register-link" to="/register">Register</router-link></a>       
                </div>
            </div>
        </div>
    </div>
</template>
 
<script>
 
    import axios from "axios"
    import swal from "sweetalert2"
 
    export default {
        data() {
            return {
                "isLoading": false
            };
        },
 
        methods: {
            doLogin: async function () {
                const self = this
                const form = event.target
                const formData = new FormData(form)
 
                this.isLoading = true;
 
                const response = await axios.post(
                    this.$apiURL + "/login",
                    formData
                );
 
                if (response.data.status == "success") {
                    // get access token from server
                    var accessToken = response.data.accessToken;
 
                    // save in local storage
                    localStorage.setItem(this.$accessTokenKey, accessToken)
 
                    // to go to home page without refreshing
                    // this.$router.push("/")
 
                    setTimeout(function () {
                        window.location.href = "/"
                    }, 500);
 
                    form.reset()
                } else {
                    this.isLoading = false;
                    swal.fire("Error", response.data.message, "error");
                }
            }
        }
    }
</script>
